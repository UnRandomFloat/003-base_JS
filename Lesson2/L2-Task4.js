;"use strict";
/**
 * Сумма переданных числе
 * @param {Number} x 
 * @param {Number} y 
 * @returns 
 */
function sumNums(x, y){
return x+y;
}

/**
 * Разность преданных чисел
 * @param {Number} x 
 * @param {Number} y 
 * @returns 
 */
function subNums(x, y){
return x-y;
}
/**
 * Произведение чисел
 * @param {Number} x 
 * @param {Number} y 
 * @returns 
 */
function mulNums(x, y){
return x*y;
}

/**
 * Частное чисел
 * @param {Number} x - числитель 
 * @param {Number} y - знаминатель 
 * @returns 
 */
function divNums(x, y){
return x/y;
}
